#!/usr/bin/env node
/**
 * Build step: read content/refs.yaml → emit src/generated/citations.ts
 * Citation data must be importable from BOTH server and client bundles.
 * Reading via node:fs at runtime breaks Turbopack client chunks, so we
 * pre-render the registry into TypeScript before next build / next dev.
 */

import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";
import yaml from "js-yaml";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..");
const SRC_REFS = join(ROOT, "content/refs.yaml");
const OUT_PATH = join(ROOT, "src/generated/citations.ts");

const raw = readFileSync(SRC_REFS, "utf-8");
const parsed = yaml.load(raw);

if (!parsed || typeof parsed !== "object") {
  console.error("[generate-citations] refs.yaml is empty or invalid");
  process.exit(1);
}

mkdirSync(dirname(OUT_PATH), { recursive: true });

const banner = `/* AUTO-GENERATED from content/refs.yaml. Do not edit by hand.
 * Regenerated by scripts/generate-citations.mjs (runs in prebuild + predev).
 */\n`;

const ts = `${banner}
import type { Citation } from "@/lib/schemas/citation";

export const CITATIONS: Record<string, Citation> = ${JSON.stringify(parsed, null, 2)} as const;
`;

writeFileSync(OUT_PATH, ts);
console.log(
  `[generate-citations] wrote ${Object.keys(parsed).length} citations → ${OUT_PATH}`
);
